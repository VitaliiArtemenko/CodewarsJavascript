const sortByHeightTest = require('./sortByHeight');

describe('Simple Fun #88: Sort By Height', () => {

    it('Test_1', () => {
        expect(sortByHeightTest([-1, 150, 190, 170, -1, -1, 160, 180]))
            .toEqual([-1, 150, 160, 170, -1, -1, 180, 190]);
    });

    it('Test_2', () => {
        expect(sortByHeightTest([-1, -1, -1, -1, -1])).toEqual([-1, -1, -1, -1, -1]);
    });

    it('Test_3', () => {
        expect(sortByHeightTest([4, 2, 9, 11, 2, 16])).toEqual([2, 2, 4, 9, 11, 16]);
    });

    it('Test_4', () => {
        expect(sortByHeightTest([ 162, 122, 120, 106, 140, 114, 94, 151, 149, 104, 124, -1, 102, 116, 172 ]))
            .toEqual([ 94, 102, 104, 106, 114, 116, 120, 122, 124, 140, 149, -1, 151, 162, 172 ]);
    });

    it('Test_5', () => {
        expect(sortByHeightTest([ -1, 197, 178, 111, 110, -1, -1, 141, 168, 195, -1, 153, 107, 188, 87, 137, 114,
            99, 134, 162, 176, -1, 116, 184, 93, 141, 194, 166, 161 ]))
            .toEqual([-1, 87, 93, 99, 107, -1, -1, 110, 111, 114, -1, 116, 134, 137, 141, 141,
                153, 161, 162, 166, 168, -1, 176, 178, 184, 188, 194, 195, 197]);
    });
});
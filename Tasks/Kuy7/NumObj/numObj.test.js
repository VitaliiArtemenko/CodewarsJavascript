const numObjTest = require('./numObj');

describe('Numbers to Objects', () => {

    it('Test_1', () => {
        expect(numObjTest([118,117,120])).toEqual([{'118':'v'}, {'117':'u'}, {'120':'x'}]);
    });

    it('Test_2', () => {
        expect(numObjTest([101,121,110,113,113,103]))
            .toEqual([{'101':'e'}, {'121':'y'}, {'110':'n'}, {'113':'q'}, {'113':'q'}, {'103':'g'}]);
    });

    it('Test_3', () => {
        expect(numObjTest([ 105, 117, 108, 114, 110, 98, 104, 114, 110, 118, 107, 108, 109, 113,  98, 109 ]))
            .toEqual([ { '105': 'i' }, { '117': 'u' }, { '108': 'l' }, { '114': 'r' }, { '110': 'n' },
                { '98': 'b' }, { '104': 'h' }, { '114': 'r' }, { '110': 'n' }, { '118': 'v' }, { '107': 'k' },
                { '108': 'l' }, { '109': 'm' }, { '113': 'q' }, { '98': 'b' }, { '109': 'm' } ]);
    });

    it('Test_4', () => {
        expect(numObjTest([ 104, 119, 101, 111, 106, 115, 113, 115, 115, 111 ]))
            .toEqual([ { '104': 'h' }, { '119': 'w' }, { '101': 'e' }, { '111': 'o' }, { '106': 'j' },
                { '115': 's' }, { '113': 'q' }, { '115': 's' }, { '115': 's' }, { '111': 'o' } ]);
    });

    it('Test_5', () => {
        expect(numObjTest([ 100, 105, 117, 109, 111, 122 ]))
            .toEqual([ { '100': 'd' }, { '105': 'i' }, { '117': 'u' }, { '109': 'm' }, { '111': 'o' }, { '122': 'z' } ]);
    });
});
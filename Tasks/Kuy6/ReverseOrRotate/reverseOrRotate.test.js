const reverseOrRotateTest = require('./reverseOrRotate');

describe('Reverse or rotate?', () => {

    it('Test_1', () => {
        expect(reverseOrRotateTest('1234 ', 0)).toEqual('');
    })

    it('Test_2', () => {
        expect(reverseOrRotateTest('', 0)).toEqual('');
    })

    it('Test_3', () => {
        expect(reverseOrRotateTest('733049910872815764', 5)).toEqual('330479108928157');
    })

    it('Test_4', () => {
        expect(reverseOrRotateTest('73304991087281576455176044327690580265896', 8))
            .toEqual('1994033775182780067155464327690480265895');
    })

    it('Test_5', () => {
        expect(reverseOrRotateTest('73304991087281576455176044327690580265896896028', 8))
            .toEqual('1994033775182780067155464327690480265895');
    })

    it('Test_6', () => {
        expect(reverseOrRotateTest('73304991087281576455176044327690580265896896028126182265439', 11))
            .toEqual('3304991087781576455172509672344060265896896862281621820');
    })

    it('Test_7', () => {
        expect(reverseOrRotateTest('73304991087281576455176044327690580265896896028126182265439441215340989', 14))
            .toEqual('1827801994033776455176044325690580265896875622816218206939441215340984');
    })

    it('Test_8', () => {
        expect(reverseOrRotateTest('563000655734469485', 4)).toEqual('0365065073456944');
    })

    it('Test_9', () => {
        expect(reverseOrRotateTest('56300065573446948588855', 3)).toEqual('365000556437694854888');
    })

    it('Test_10', () => {
        expect(reverseOrRotateTest('56300065573446948588855200928875449742090', 6))
            .toEqual('000365437556584964255888092880794457');
    })

    it('Test_11', () => {
        expect(reverseOrRotateTest('56300065573446948588855200928875449742090827299285754137212', 11))
            .toEqual('3755600036546948588854457882900257280902479499285754132');
    })

    it('Test_12', () => {
        expect(reverseOrRotateTest('56300065573446948588855200928875449742090827299285754137212673841954794395427', 10))
            .toEqual('6300065575446948588355200928885449742097582992728062127314573841954797');
    })
})
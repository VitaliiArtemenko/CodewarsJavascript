const isVowTest = require('./isVow');

describe('Is there a vowel in there?', () => {

    it('Test_1', () => {
        expect(isVowTest([118,117,120,121,117,98,122,97,120,106,104,116,113,114,113,120,106]))
            .toEqual([118,"u",120,121,"u",98,122,"a",120,106,104,116,113,114,113,120,106]);
    });

    it('Test_2', () => {
        expect(isVowTest([101,121,110,113,113,103,121,121,101,107,103]))
            .toEqual(["e",121,110,113,113,103,121,121,"e",107,103]);
    });

    it('Test_3', () => {
        expect(isVowTest([ 115, 110, 119, 120, 102, 120, 116, 109, 99, 108, 118, 112, 102, 100 ]))
            .toEqual([ 115, 110, 119, 120, 102, 120, 116, 109, 99, 108, 118, 112, 102, 100 ]);
    });

    it('Test_4', () => {
        expect(isVowTest([ 97, 101, 103, 117, 98, 116 ])).toEqual([ 'a', 'e', 103, 'u', 98, 116 ]);
    });

    it('Test_5', () => {
        expect(isVowTest([97, 97])).toEqual(['a', 'a']);
    });
});